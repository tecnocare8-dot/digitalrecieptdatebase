export function predictCategory(companyName: string): string {
    if (!companyName) return '未分類';

    for (const [keyword, category] of Object.entries(CATEGORY_KEYWORDS)) {
        if (companyName.includes(keyword)) {
            return category;
        }
    }

    return '未分類';
}
